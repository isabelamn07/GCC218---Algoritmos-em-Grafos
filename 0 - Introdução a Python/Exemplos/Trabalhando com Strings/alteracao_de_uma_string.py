# Alteração de uma string


S = "hello world!"
#Para acessar qualquer posição da string usamos colchetes e o índice do caractere
print(S[0])

#Não é possível modificar apenas uma posição da string, 
S[0] = 'H'
print(S)

# Para "mudar" o valor da string devemos atribuir outro valor
S = "Hello world!"
print(S)

